public class Solution {
    public boolean isSameTree(TreeNode p, TreeNode q) {
        if (p == null && q == null) {
            return true;
        }
        if (p == null||q==null) {
            return  false;
        }

        if (p.val != q.val) {
            return  false;
        }

        boolean sameTree = isSameTree(p.left, q.left);

        if (sameTree) {
            sameTree = isSameTree(p.right, q.right);
        }

        return sameTree;

    }
}
